    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        if(Password.getText().equals(Registrasi.password)&&(Username.getText().equals(Registrasi.username))){
            //masuk ke CRUD
            Crud_Program crd = new Crud_Program();
            crd.setVisible(true);
            crd.pack();
            crd.setLocationRelativeTo(null);
            crd.setDefaultCloseOperation(Registrasi.EXIT_ON_CLOSE);
        }
        else if(!Password.getText().equals(Registrasi.password)&&(Username.getText().equals(Registrasi.username))){
            peringatan.setText("Password yang Anda Masukkan salah !!!");
        }
        else{
            peringatan.setText("Username Anda salah atau belum terdaftar !!!");
        }
    }//GEN-LAST:event_LoginActionPerformed

